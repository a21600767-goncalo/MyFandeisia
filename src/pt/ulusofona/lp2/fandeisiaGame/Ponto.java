package pt.ulusofona.lp2.fandeisiaGame;

public class Ponto {

    int x;
    int y;

    Ponto(){}

    Ponto( int x, int y){

        this.x=x;
        this.y=y;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public int setX(int x){
        return x;
    }

    public int setY(int y){
        return y;
    }

}
